    private void setTextFontSize() {
        try {
            Font font = graphPanel.getGrapher().getFont();
            double gridSize = graphPanel.getGrapher().getGridSize();
            double size = font.getSize();
            double next = Double.valueOf(gridSizeSpinner.getNextValue().toString());
            if (size > next) {
                size--;
            } else {
                size++;
            }
            graphPanel.getGrapher().setFont(new Font(font.getFamily(), font.getStyle(), (int) size));
        }//end try
        catch (NullPointerException nolian) {

        }
    }

