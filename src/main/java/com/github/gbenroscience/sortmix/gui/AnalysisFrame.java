    /**
     * Saves a BufferedImage to the given file, pathname must not have any
     * periods "." in it except for the one before the format, i.e.
     * C:/images/fooimage.png
     *
     * @param img The image to save
     * @param ref The path to save it to.
     */
    public static void saveImage(BufferedImage img, String ref) {
        try {
            String format = (ref.endsWith(".png")) ? "png" : "jpg";
            ImageIO.write(img, format, new File(ref));
        } catch (IOException e) {
            e.printStackTrace();
        }
    }

    private int inputDataTypeAlgorithmSelect() {
        if (randomDataFloatCheckBoxMenuItem.isSelected()) {
            return Grapher.Graphed.ARRAY_OF_RANDOM_FLOATS;
        } else if (reverseSortedDataFloatCheckBoxMenuItem.isSelected()) {
            return Grapher.Graphed.ARRAY_OF_REVERSE_SORTED_FLOATS;
        }//ty
        else if (partiallySortedDataFloatCheckBoxMenuItem.isSelected()) {
            return Grapher.Graphed.ARRAY_OF_PARTIALLY_SORTED_FLOATS;
        } else if (sortedDataFloatCheckBoxMenuItem.isSelected()) {
            return Grapher.Graphed.ARRAY_OF_SORTED_FLOATS;
        } else if (binaryArrayDataFloatCheckBoxMenuItem.isSelected()) {
            return Grapher.Graphed.BINARY_ARRAY_OF_FLOATS;
        } else if (randomDataIntCheckBoxMenuItem.isSelected()) {
            return Grapher.Graphed.ARRAY_OF_RANDOM_INTS;
        } else if (reverseSortedDataIntCheckBoxMenuItem.isSelected()) {
            return Grapher.Graphed.ARRAY_OF_REVERSE_SORTED_INTS;
        } else if (partiallySortedDataIntCheckBoxMenuItem.isSelected()) {
            return Grapher.Graphed.ARRAY_OF_PARTIALLY_SORTED_INTS;
        } else if (sortedDataIntCheckBoxMenuItem.isSelected()) {
            return Grapher.Graphed.ARRAY_OF_SORTED_INTS;
        } else if (binaryArrayDataIntCheckBoxMenuItem.isSelected()) {
            return Grapher.Graphed.BINARY_ARRAY_OF_INTS;
        } else if (digitsArray0_9_DataCheckBoxMenuItem.isSelected()) {
            return Grapher.Graphed.ARRAY_OF_DIGITS;
        }
        return -1;

    }

    private void setVerticalScale() {
        try {
            double vertical = Double.valueOf(verticalScaleTextField.getText());
            graphPanel.getGrapher().setyStep(vertical);
            graphPanel.getGrapher().setRefresh(true);
        }//end try
        catch (NumberFormatException numErr) {
            JOptionPane.showMessageDialog(null, "PLEASE ENTER A POSITIVE INTEGER!.", "BAD VALUE(S) On Vertical Settings", JOptionPane.ERROR_MESSAGE);
        }//end catch
    }//end method

