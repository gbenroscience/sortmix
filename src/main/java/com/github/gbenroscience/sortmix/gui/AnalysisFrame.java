    private boolean busy = false;

    private void plotComparisonsButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_plotComparisonsButtonActionPerformed
        if (busy) {
            JOptionPane.showMessageDialog(this, "Analysis in progress", "Appliation is busy!", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        Thread runner = new Thread(new Runnable() {

            @Override
            public void run() {
                busy = true;
                String text = getTitle();
                setTitle(text + "..Please wait!");

                //applySettings();     
                if (arraysdotSortCheckBoxMenuItem.isSelected()) {
                    graphPanel.getGrapher().addGraphed(graphPanel.getGrapher().new Graphed(Grapher.Graphed.JAVA_ARRAYS_DOT_SORT, inputDataTypeAlgorithmSelect()));
                } else {
                    graphPanel.getGrapher().removeGraphed(Grapher.Graphed.JAVA_ARRAYS_DOT_SORT);
                }
                if (speedAndCacheBucketSortCheckBoxMenuItem.isSelected()) {
                    graphPanel.getGrapher().addGraphed(
                            graphPanel.getGrapher().new Graphed(Grapher.Graphed.CACHE_OPTIMIZATION_ENABLED_SPEED_OPTIMIZED_BUCKETSORT, inputDataTypeAlgorithmSelect()));
                } else {
                    graphPanel.getGrapher().removeGraphed(Grapher.Graphed.CACHE_OPTIMIZATION_ENABLED_SPEED_OPTIMIZED_BUCKETSORT);
                }
                if (memoryAndCacheBucketSortCheckBoxMenuItem.isSelected()) {
                    graphPanel.getGrapher().addGraphed(graphPanel.getGrapher().new Graphed(
                            Grapher.Graphed.CACHE_OPTIMIZATION_ENABLED_MEMORY_OPTIMIZED_BUCKETSORT, inputDataTypeAlgorithmSelect()));
                } else {
                    graphPanel.getGrapher().removeGraphed(Grapher.Graphed.CACHE_OPTIMIZATION_ENABLED_MEMORY_OPTIMIZED_BUCKETSORT);
                }
                if (speedOnlyCheckBoxMenuItem.isSelected()) {
                    graphPanel.getGrapher().addGraphed(
                            graphPanel.getGrapher().new Graphed(Grapher.Graphed.CACHE_OPTIMIZATION_DISABLED_SPEED_OPTIMIZED_BUCKETSORT, inputDataTypeAlgorithmSelect()));
                } else {
                    graphPanel.getGrapher().removeGraphed(Grapher.Graphed.CACHE_OPTIMIZATION_DISABLED_SPEED_OPTIMIZED_BUCKETSORT);
                }
                if (memoryOnlyCheckBoxMenuItem.isSelected()) {
                    graphPanel.getGrapher().addGraphed(
                            graphPanel.getGrapher().new Graphed(Grapher.Graphed.CACHE_OPTIMIZATION_DISABLED_MEMORY_OPTIMIZED_BUCKETSORT, inputDataTypeAlgorithmSelect()));
                } else {
                    graphPanel.getGrapher().removeGraphed(Grapher.Graphed.CACHE_OPTIMIZATION_DISABLED_MEMORY_OPTIMIZED_BUCKETSORT);
                }
                if (heapSortCheckBoxMenuItem.isSelected()) {
                    graphPanel.getGrapher().addGraphed(graphPanel.getGrapher().new Graphed(Grapher.Graphed.HEAPSORT, inputDataTypeAlgorithmSelect()));
                } else {
                    graphPanel.getGrapher().removeGraphed(Grapher.Graphed.HEAPSORT);
                }
                if (insertionSortCheckBoxMenuItem.isSelected()) {
                    graphPanel.getGrapher().addGraphed(graphPanel.getGrapher().new Graphed(Grapher.Graphed.INSERTIONSORT, inputDataTypeAlgorithmSelect()));
                } else {
                    graphPanel.getGrapher().removeGraphed(Grapher.Graphed.INSERTIONSORT);
                }
                if (mergeSortCheckBoxMenuItem.isSelected()) {
                    graphPanel.getGrapher().addGraphed(graphPanel.getGrapher().new Graphed(Grapher.Graphed.MERGESORT, inputDataTypeAlgorithmSelect()));
                } else {
                    graphPanel.getGrapher().removeGraphed(Grapher.Graphed.MERGESORT);
                }
                if (quickSortCheckBoxMenuItem.isSelected()) {
                    graphPanel.getGrapher().addGraphed(graphPanel.getGrapher().new Graphed(Grapher.Graphed.QUICKSORT, inputDataTypeAlgorithmSelect()));
                } else {
                    graphPanel.getGrapher().removeGraphed(Grapher.Graphed.QUICKSORT);
                }
                if (selectionSortCheckBoxMenuItem.isSelected()) {
                    graphPanel.getGrapher().addGraphed(graphPanel.getGrapher().new Graphed(Grapher.Graphed.SELECTIONSORT, inputDataTypeAlgorithmSelect()));
                } else {
                    graphPanel.getGrapher().removeGraphed(Grapher.Graphed.SELECTIONSORT);
                }
                if (shellSortCheckBoxMenuItem.isSelected()) {
                    graphPanel.getGrapher().addGraphed(graphPanel.getGrapher().new Graphed(Grapher.Graphed.SHELLSORT, inputDataTypeAlgorithmSelect()));
                } else {
                    graphPanel.getGrapher().removeGraphed(Grapher.Graphed.SHELLSORT);
                }

                setTitle(text);
                graphPanel.getGrapher().setRefresh(true);
                repaint();
                busy = false;
            }
        });
        runner.setPriority(Thread.MAX_PRIORITY);
        runner.start();
    }//GEN-LAST:event_plotComparisonsButtonActionPerformed

